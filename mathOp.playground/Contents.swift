// MATH OPERATORS
// Adding and Subracting (+ is for Addition. - is for Subtraction)
print(2 + 2) // 4
print(2.25 + 2.5 + 1.5) // 6.25
print(3 - 2) // 1
print(4 - 6) // -2

// Multiplying and Dividing (* is for Multiplication. / is for Division)
print(4 * 5) // 20
print(2.5 * 0.5) // 1.25
print(15 / 3) // 5

// When using Math operators in Swift the result will be in Int format (AKA No decimals) when Int data types are the only data type used in the equation
print(1 / 4) // 0

// Displays the decimal result since the numbers (AKA Data Types) used in the equation are Float data types instead of Int data types
print(1.0 / 4.0) // 0.25

// Modulo (AKA Remainder)
// Percentage Sign % is used for Modulo
// Returns the division remainder
print(9 % 4) // 1
print(23 % 7) // 2
// Since 5 cannot go into 4 the remainder is 4
print(4 % 5) // 4
